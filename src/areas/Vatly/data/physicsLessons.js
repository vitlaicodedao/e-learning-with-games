// Hàm trợ giúp đếm tổng số bài và bài đã hoàn thành
export const getLessonStats = (classId, userProgress = {}) => {
  const classData = physicsLessons[classId];
  if (!classData) return { total: 0, completed: 0 };

  let total = 0;
  let completed = 0;

  classData.chapters.forEach(chapter => {
    chapter.lessons.forEach(lesson => {
      total++;
      const lessonKey = `${classId}-${chapter.id}-${lesson.id}`;
      if (userProgress[lessonKey] === 'completed') {
        completed++;
      }
    });
  });

  return { total, completed };
};

// Hàm trợ giúp lấy trạng thái bài học
export const getLessonStatus = (classId, chapterId, lessonId, userProgress = {}) => {
  const lessonKey = `${classId}-${chapterId}-${lessonId}`;
  return userProgress[lessonKey] || 'not-started';
};

// Hàm trợ giúp cập nhật trạng thái bài học
export const updateLessonStatus = (classId, chapterId, lessonId, status, userProgress = {}) => {
  const lessonKey = `${classId}-${chapterId}-${lessonId}`;
  return {
    ...userProgress,
    [lessonKey]: status
  };
};
